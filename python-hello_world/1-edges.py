#!/usr/bin/python3
word = "Holberton"

# Extract first 3 letters of the variable 'word'
word_first_3 = word[:3]

# Extract last 2 letters of the variable 'word'
word_last_2 = word[-2:]

# Get 'middle_word' without the first and last letters
middle_word = word[1:-1]

print(word_first_3)
print(word_last_2)
print(middle_word)
